import type { ResolvedGloveboxConfig } from "../config"
import type { Manifest, ManifestEnvVar } from "../protocol"

export interface ManifestArgs {
  config: ResolvedGloveboxConfig
  keyFingerprint: string
}

export function generateManifest(args: ManifestArgs): Manifest {
  const { config, keyFingerprint } = args

  const env: Record<string, ManifestEnvVar> = {}
  for (const [name, spec] of Object.entries(config.env)) {
    env[name] = {
      required: spec.required,
      secret: spec.secret,
      default: spec.default,
      description: spec.description,
    }
  }

  return {
    name: config.name,
    version: config.version,
    base: config.base,
    fs: config.fs,
    env,
    limits: config.limits,
    key_fingerprint: keyFingerprint,
    storage_policy: config.storage,
    packages: config.packages,
    protocol_version: 1,
  }
}

export function generateEnvExample(config: ResolvedGloveboxConfig): string {
  const lines: string[] = []
  lines.push("# Auth key for the glovebox server (generated by `glovebox build`)")
  lines.push("GLOVEBOX_KEY=")
  lines.push("")
  lines.push("# Optional: override the listening port (default 8080)")
  lines.push("# GLOVEBOX_PORT=8080")
  lines.push("")
  for (const [name, spec] of Object.entries(config.env)) {
    if (spec.description) lines.push(`# ${spec.description}`)
    if (spec.required) lines.push("# (required)")
    if (spec.secret) lines.push("# (secret)")
    lines.push(`${name}=${spec.default ?? ""}`)
    lines.push("")
  }
  return lines.join("\n")
}
